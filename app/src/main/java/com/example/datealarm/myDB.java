package com.example.datealarm;

public class myDB {
    private String name;
    private String date;

    public myDB() {
    }

    public myDB(String name, String date) {
        this.name = name;
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

}
